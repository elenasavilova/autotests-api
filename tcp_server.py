import socket  # Импортируем модуль socket для работы с сетевыми соединениями


def server():
    # Создаем TCP-сокет
    """
    AF_INET — используем IPv4. SOCK_STREAM — задаем тип сокета как TCP.
    """
    server_socket = socket.socket(socket.AF_INET, socket.SOCK_STREAM)

    # Привязываем его к адресу и порту
    """
    bind() связывает сокет с определенным IP-адресом и портом.
    localhost (127.0.0.1) означает, что сервер работает только на локальном компьютере.
    """
    server_address = ('localhost', 12345)
    server_socket.bind(server_address)

    # Начинаем слушать входящие подключения (максимум 5 в очереди)
    """
    Запускаем режим прослушивания (listen()), указывая, что сервер может ожидать до 5 подключений в очереди.
    Если к серверу обратится более 5 клиентов одновременно, они будут ждать.
    """
    server_socket.listen(5)
    print("Сервер запущен и ждет подключений...")

    while True: # Запускаем бесконечный цикл, чтобы сервер всегда ждал новые подключения.
        # Принимаем соединение от клиента
        """
        accept() останавливает выполнение и ждет подключения клиента. Когда клиент подключается:
        Возвращается новый сокет client_socket для общения с этим клиентом, client_address содержит IP и порт клиента.
        """
        client_socket, client_address = server_socket.accept()
        print(f"Подключение от {client_address}") # Выводим в консоль, кто подключился.

        # Получаем данные от клиента
        """
        recv(1024) ждет и получает данные от клиента, максимум 1024 байта, .decode() — преобразует байты в строку 
        (обычно в UTF-8).
        """
        data = client_socket.recv(1024).decode()
        print(f"Получено сообщение: {data}")

        # Отправляем ответ клиенту
        """
        Формируем ответ сервером и отправляем его клиенту,
        encode() — преобразует строку в байты, так как send() принимает только байты.
        """
        response = f"Сервер получил: {data}"
        client_socket.send(response.encode())

        # Закрываем соединение с клиентом
        """
        Важно! Это закрывает только этого клиента, а сервер продолжает работать.
        """
        client_socket.close()

# Запуск TCP-сервера
"""
Проверяем, запущен ли файл напрямую. Если да, вызываем server(), и сервер начинает работу.
"""
if __name__ == '__main__':
    server()

# Сервер запускается и начинает прослушивать порт 12345.
# Ожидает подключения клиента (accept()).
# При получении сообщения:
# Выводит его в консоль.
# Отправляет клиенту ответ.
# Закрывает соединение с клиентом и ждет нового.
# Теперь сервер можно запустить командой:
# python -m tcp_server